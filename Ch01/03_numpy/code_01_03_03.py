import numpy as np

arr = np.array([10, 20, 30, 40, 50])

print(arr[0])    # 첫 번째 원소
print(arr[-1])   # 마지막 원소
print(arr[1:4])  # 인덱스 1~3 슬라이싱
print(arr[::2])  # 2칸씩 건너뛰며 선택
