import numpy as np

a = np.array([1, 2, 3, 4, 5])
b = np.array([10, 20, 30, 40, 50])

# 사칙연산 (원소별 적용)
print(a + 10)    # 스칼라 덧셈 (브로드캐스팅)
print(a * 2)     # 스칼라 곱셈
print(a + b)     # 배열 간 덧셈

# 내적 (dot product) — 벡터의 유사도, 공분산 계산에 사용
print(np.dot(a, b))
