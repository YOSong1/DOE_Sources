# sample_data.xlsx의 dice_events 시트(2000회 주사위, 사건 컬럼 포함)를
# 읽어 다음 공리적 성질을 명시적으로 검증:
#   1) 비음수성  P(A) >= 0
#   2) 정규화    P(Ω) = 1
#   3) 가산 가법성 (배반)
#   4) 공사건    P(∅) = 0
#   5) 여사건    P(A^c) = 1 - P(A)
#   6) 합사건    P(A∪B) = P(A) + P(B) - P(A∩B)
# =============================================================================

import os
import sys
import io
import numpy as np
import pandas as pd

# Windows 콘솔에서 한글/특수문자(∅, —) 출력을 위해 UTF-8 강제
if sys.platform == "win32":
    sys.stdout = io.TextIOWrapper(sys.stdout.buffer, encoding="utf-8")

BASE = os.path.dirname(os.path.abspath(__file__))
XLSX = os.path.join(BASE, "sample_data.xlsx")

# -----------------------------------------------------------------------------
# 1) Excel 시행 로그 읽기
# -----------------------------------------------------------------------------
df = pd.read_excel(XLSX, sheet_name="dice_events")
n = len(df)

print("=== dice_events 시트 (앞 5개) ===")
print(df.head())
print(f"\n총 시행: {n}회")
print(f"\n각 사건의 발생 횟수 (1/0):")
print(f"  A(짝수)   = {df['is_even'].sum()}회")
print(f"  B(3의배수) = {df['is_mult3'].sum()}회")
print(f"  A^c(홀수) = {df['is_odd'].sum()}회")

# -----------------------------------------------------------------------------
# 2) 공리 1: 비음수성 — 모든 확률은 0 이상
# -----------------------------------------------------------------------------
print("\n=== 공리 1: 비음수성 P(A) >= 0 ===")
p_A = df["is_even"].mean()
p_B = df["is_mult3"].mean()
p_Ac = df["is_odd"].mean()
for name, p in [("P(A 짝수)", p_A), ("P(B 3의배수)", p_B),
                ("P(A^c 홀수)", p_Ac)]:
    print(f"  {name} = {p:.4f}  >=0? {p >= 0}")

# -----------------------------------------------------------------------------
# 3) 공리 2: 정규화 — P(Ω) = 1
# -----------------------------------------------------------------------------
print("\n=== 공리 2: P(Ω) = 1 ===")
p_omega = df["value"].isin([1, 2, 3, 4, 5, 6]).mean()
print(f"  P({{1,2,3,4,5,6}}) = {p_omega:.4f}  (이론: 1.0000)")

# -----------------------------------------------------------------------------
# 4) 공사건: P(7) = 0
# -----------------------------------------------------------------------------
print("\n=== 공사건: P(∅) = 0 ===")
p_seven = (df["value"] == 7).mean()
print(f"  P(7) = {p_seven:.4f}  (이론: 0.0000)")

# -----------------------------------------------------------------------------
# 5) 여사건: P(A^c) = 1 - P(A)
# -----------------------------------------------------------------------------
print("\n=== 여사건: P(A^c) = 1 - P(A) ===")
print(f"  P(A)              = {p_A:.4f}")
print(f"  P(A^c) 데이터     = {p_Ac:.4f}")
print(f"  1 - P(A) 공식     = {1 - p_A:.4f}")
print(f"  P(A) + P(A^c)     = {p_A + p_Ac:.4f}  (이론: 1.0000)")

# -----------------------------------------------------------------------------
# 6) 합사건의 덧셈 법칙: P(A∪B) = P(A) + P(B) - P(A∩B)
# -----------------------------------------------------------------------------
print("\n=== 덧셈 법칙: P(A∪B) = P(A) + P(B) - P(A∩B) ===")
# 교집합과 합집합 계산
inter = (df["is_even"] & df["is_mult3"]).astype(int)   # A∩B = {6}
union = (df["is_even"] | df["is_mult3"]).astype(int)   # A∪B = {2,3,4,6}

p_AB = inter.mean()
p_union_emp = union.mean()
p_union_formula = p_A + p_B - p_AB

print(f"  P(A)          = {p_A:.4f}  (이론 3/6 = {3/6:.4f})")
print(f"  P(B)          = {p_B:.4f}  (이론 2/6 = {2/6:.4f})")
print(f"  P(A∩B)        = {p_AB:.4f}  (이론 1/6 = {1/6:.4f})")
print(f"  P(A∪B) 데이터  = {p_union_emp:.4f}")
print(f"  P(A∪B) 공식    = P(A)+P(B)-P(A∩B) = {p_union_formula:.4f}")
print(f"  이론값         = 4/6 = {4/6:.4f}")

# -----------------------------------------------------------------------------
# 7) 결과 요약
# -----------------------------------------------------------------------------
print("\n=== 검증 결과 요약 ===")
checks = {
    "공리 1 비음수성": all(p >= 0 for p in [p_A, p_B, p_Ac]),
    "공리 2 정규화":  abs(p_omega - 1) < 1e-6,
    "공사건 P(7)=0":  abs(p_seven) < 1e-6,
    "여사건 합=1":    abs(p_A + p_Ac - 1) < 1e-6,
    "덧셈 법칙 일치": abs(p_union_emp - p_union_formula) < 1e-6,
}
for name, ok in checks.items():
    status = "OK" if ok else "FAIL"
    print(f"  [{status}] {name}")

print("\n[해석] 2000회의 실제 시행 데이터에서도 콜모고로프 공리와 그로부터\n"
      "        유도되는 성질들이 모두 성립함을 수치로 확인했습니다.")
