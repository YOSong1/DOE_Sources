# 설명: 동전과 주사위 시뮬레이션으로 시행 횟수가 늘수록 상대 빈도가
#       이론값 0.5에 수렴하는 것을 확인.
# =============================================================================

import numpy as np

# 재현성을 위한 시드 고정
np.random.seed(42)

# 1. 동전 던지기 시뮬레이션 (0=뒷면, 1=앞면)
print("=== 동전 던지기 시뮬레이션 ===")
for n in [10, 100, 1000, 10000]:
    flips = np.random.randint(0, 2, size=n)
    freq = flips.mean()
    print(f"시행 횟수: {n:6d} | 앞면 빈도: {freq:.4f} | 이론값: 0.5000")

print()

# 2. 주사위 던지기 시뮬레이션 (짝수 사건)
print("=== 주사위 던지기 — 짝수 빈도 수렴 ===")
for n in [10, 100, 1000, 10000]:
    rolls = np.random.randint(1, 7, size=n)
    freq_even = np.mean(rolls % 2 == 0)
    print(f"시행 횟수: {n:6d} | 짝수 빈도: {freq_even:.4f} | 이론값: 0.5000")
